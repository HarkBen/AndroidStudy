/**
 * 类、对象、接口、构造函数、方法、属性和它们的 setter 都可以有 可见性修饰符。
 * （getter 总是与属性有着相同的可见性。）
 * 在 Kotlin 中有这四个可见性修饰符：private、 protected、 internal 和 public。
 * 如果没有显式指定修饰符的话，默认可见性是 public。
 *
 * 请注意在 Kotlin 中，外部类不能访问内部类的 private 成员。
 */